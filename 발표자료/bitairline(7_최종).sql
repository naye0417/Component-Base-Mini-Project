CREATE TABLE passenger
(
   acc_num               NUMBER  NOT NULL ,
   name                  VARCHAR2(20)  NULL ,
   sex                   VARCHAR2(3)  NULL ,
   rrn                   VARCHAR(20)  NULL ,
   phone                 VARCHAR2(20)  NULL,
   CONSTRAINT acc_num_pk PRIMARY KEY (acc_num)
);

CREATE TABLE acc
(
   acc_num               NUMBER  NOT NULL ,
   ID                    VARCHAR2(12)  NULL ,
   PW                    VARCHAR2(12)  NULL, 
   CONSTRAINT acc_num_fk FOREIGN KEY (acc_num) REFERENCES passenger (acc_num)
);

CREATE TABLE seat (
  flight_name varchar2(7),
  grade varchar2(20),
  price  NUMBER
);

CREATE TABLE flight (
  flight_name varchar2(7), 
  airline_name varchar2(20),
  total_seat NUMBER
);

CREATE TABLE reservation
(
   reservation_num       NUMBER  NOT NULL ,
   acc_num           NUMBER  NOT NULL ,
   schedule_num          VARCHAR2(20) NOT NULL ,
   reservation_date      DATE  NULL ,
   seat_num              VARCHAR2(10),
   price                 NUMBER  NULL
);

CREATE TABLE schedule
(  
schedule_num VARCHAR2(20) NOT NULL,  
flight_name VARCHAR2(7) NOT NULL,
departure VARCHAR2(12) NULL,
arrival VARCHAR2(12) NULL,
departure_datetime DATE NULL,
arrival_datetime DATE NULL
);

CREATE TABLE cart
(  
cart_num NUMBER NOT NULL, 
acc_num  NUMBER  NOT NULL ,
schedule_num VARCHAR2(20) NOT NULL,  
flight_name VARCHAR2(7) NOT NULL,
departure VARCHAR2(12) NULL,
arrival VARCHAR2(12) NULL,
departure_datetime DATE NULL,
arrival_datetime DATE NULL,

CONSTRAINT cart_num_pk PRIMARY KEY (cart_num)
);

